
public class Tipo_datos {
	
	public static void main(String[] args) {
		
		//8 tipos de datos primitivos
		byte numPequeno = 100;
		short numMediano = 32000;
		int num = 15;
		long numGrande = 100000;
		
		float numDecimal = 3.2f;
		double numDGrande = 123232322.44434;
		
		boolean v = true; boolean f = false;
		
		char letra = 'l';
		
		//Dato estructurado
		String nombre = "Mitzi";
		System.out.println(numPequeno);
		System.out.println(numMediano);
		System.out.println(num);
		System.out.println(numGrande);
		
		System.out.println(numDecimal);
		System.out.println(numDGrande);

		System.out.println(v);
		System.out.println(f);
		

		System.out.println(letra);
		System.out.println(nombre);
		
	}
	
}
